package jmc.skweb.core.model.traza.report;

public class DatosTrazaWS {
	
	private String idObraSocial;
	private String nroRemito;
	private String nroAsociado;
	private String nombrePaciente;
	private String obs; 
	private String glnDestinatario;
	
	public String getIdObraSocial() {
		return idObraSocial;
	}
	public void setIdObraSocial(String idObraSocial) {
		this.idObraSocial = idObraSocial;
	}
	public String getNroRemito() {
		return nroRemito;
	}
	public void setNroRemito(String nroRemito) {
		this.nroRemito = nroRemito;
	}
	public String getNroAsociado() {
		return nroAsociado;
	}
	public void setNroAsociado(String nroAsociado) {
		this.nroAsociado = nroAsociado;
	}
	public String getNombrePaciente() {
		return nombrePaciente;
	}
	public void setNombrePaciente(String nombrePaciente) {
		this.nombrePaciente = nombrePaciente;
	}
	public String getObs() {
		return obs;
	}
	public void setObs(String obs) {
		this.obs = obs;
	}
	public String getGlnDestinatario() {
		return glnDestinatario;
	}
	public void setGlnDestinatario(String glnDestinatario) {
		this.glnDestinatario = glnDestinatario;
	}
	
	

}
